
-- TAB=ENODEB HOUR
SELECT FECHA, COUNT(*) CANTIDAD
  FROM LTE_C_ALC_UY_ENODEB_HOUR
 WHERE FECHA BETWEEN TO_DATE('&DateFrom', 'DD.MM.YYYY')
                 AND TO_DATE('&DateTo', 'DD.MM.YYYY') + 86399/86400
 GROUP BY FECHA
;

-- TAB=ENODEB DAY
select TRUNC(FECHA), count (*) CANTIDAD
from LTE_C_ALC_UY_ENODEB_DAY
WHERE FECHA BETWEEN TO_DATE('&DateFrom', 'DD.MM.YYYY')
                 AND TO_DATE('&DateTo', 'DD.MM.YYYY') + 86399/86400
GROUP BY TRUNC(FECHA)
ORDER BY TRUNC(FECHA)
;

-- TAB=ENODEB BH
